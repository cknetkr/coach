const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const SOURCE_ROOT = path.join(ROOT, 'data', 'english-dictation');
const OUTPUT_FILE = path.join(ROOT, 'assets', 'js', 'coach-english-dictation-template.generated.js');
const TOPICS_FILE = path.join(SOURCE_ROOT, 'topics.json');

function listJsonFiles(dirPath) {
  return fs.readdirSync(dirPath, { withFileTypes: true }).flatMap((entry) => {
    const fullPath = path.join(dirPath, entry.name);
    if (entry.isDirectory()) return listJsonFiles(fullPath);
    if (entry.isFile() && entry.name.endsWith('.json')) return [fullPath];
    return [];
  });
}

function readSentenceJson(filePath) {
  const raw = fs.readFileSync(filePath, 'utf8');
  const data = JSON.parse(raw);
  if (!data.id || !data.topicId || !data.text) {
    throw new Error(`Missing required fields in ${filePath}`);
  }
  return data;
}

function groupByTopic(sentences) {
  const topics = new Map();
  for (const sentence of sentences) {
    if (!topics.has(sentence.topicId)) topics.set(sentence.topicId, []);
    topics.get(sentence.topicId).push(sentence);
  }
  return topics;
}

function readTopicManifest() {
  if (!fs.existsSync(TOPICS_FILE)) return [];
  return JSON.parse(fs.readFileSync(TOPICS_FILE, 'utf8'));
}

function toRuntimeSentence(sentence) {
  return {
    id: sentence.id,
    text: sentence.text,
    tts: sentence.tts || null,
    guide: sentence.guide || null,
    commentary: sentence.commentary || null,
    fullMeaning: sentence.fullMeaning || null,
    blanks: Array.isArray(sentence.blanks) ? sentence.blanks : [],
  };
}

function buildWindowObject(groupedTopics, topicManifest) {
  const lines = [
    '// Auto-generated by scripts/build-english-dictation-template.js',
    '// Do not edit this file by hand.',
    'window.ENG_DICTATION_TOPIC_OVERRIDES = window.ENG_DICTATION_TOPIC_OVERRIDES || {};',
  ];

  const manifestMap = new Map(topicManifest.map((item) => [item.topicId, item]));
  for (const [topicId, sentences] of Array.from(groupedTopics.entries())) {
    const ordered = [...sentences].sort((a, b) => (a.order || 0) - (b.order || 0));
    const manifestEntry = manifestMap.get(topicId);
    const runtimeKey = Number.isInteger(manifestEntry?.uiIndex) ? manifestEntry.uiIndex : topicId;
    lines.push(`window.ENG_DICTATION_TOPIC_OVERRIDES[${JSON.stringify(runtimeKey)}] = ${JSON.stringify(ordered.map(toRuntimeSentence), null, 2)};`);
  }

  return `${lines.join('\n')}\n`;
}

function main() {
  if (!fs.existsSync(SOURCE_ROOT)) {
    throw new Error(`Source folder not found: ${SOURCE_ROOT}`);
  }

  const jsonFiles = listJsonFiles(SOURCE_ROOT);
  const sentenceFiles = jsonFiles.filter((filePath) => !filePath.endsWith('topics.json') && !filePath.endsWith('.schema.json'));
  const sentences = sentenceFiles.map(readSentenceJson);
  const groupedTopics = groupByTopic(sentences);
  const topicManifest = readTopicManifest();
  const output = buildWindowObject(groupedTopics, topicManifest);
  fs.writeFileSync(OUTPUT_FILE, output, 'utf8');
  console.log(`Generated ${path.relative(ROOT, OUTPUT_FILE)} from ${sentenceFiles.length} JSON file(s).`);
}

main();
